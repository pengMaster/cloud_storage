    /**
     * 从sd卡获取图片资源
     *
     * @return
     */
    public static ArrayList<Media> getImagesByPath(String filePath) {
        // 图片列表
        ArrayList<Media> imagePathList = new ArrayList<Media>();
        // 得到sd卡内image文件夹的路径   File.separator(/)
        // 得到该路径文件夹下所有的文件
        File fileAll = new File(filePath);
        File[] files = fileAll.listFiles();
        // 将所有的文件存入ArrayList中,并过滤所有图片格式的文件
        for (int i = 0; i < files.length; i++) {
            File file = files[i];
            if (checkIsImageFile(file.getPath())) {
                Media media = new Media(file.getPath(), getImageName(file.getPath()), 0L, 0, file.length(), 0, "");
                imagePathList.add(media);
            }
        }
        // 返回得到的图片列表
        return imagePathList;
    }

