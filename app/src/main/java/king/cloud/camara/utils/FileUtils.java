    /**
     * 检查扩展名，得到图片格式的文件
     *
     * @param fName 文件名
     * @return
     */
    @SuppressLint("DefaultLocale")
    public static boolean checkIsImageFile(String fName) {
        boolean isImageFile = false;
        // 获取扩展名
        String FileEnd = fName.substring(fName.lastIndexOf(".") + 1,
                fName.length()).toLowerCase();
        if (FileEnd.equals("jpg") || FileEnd.equals("png") || FileEnd.equals("gif")
                || FileEnd.equals("jpeg") || FileEnd.equals("bmp")) {
            isImageFile = true;
        } else {
            isImageFile = false;
        }
        return isImageFile;
    }

    /**
     * 检查扩展名，得到视频格式的文件
     *
     * @param fName 文件名
     * @return
     */
    @SuppressLint("DefaultLocale")
    public static boolean checkIsVideoFile(String fName) {
        boolean isImageFile = false;
        // 获取扩展名
        String name = fName.substring(fName.lastIndexOf(".") + 1,
                fName.length()).toLowerCase();
        if (name.equals("mp4") || name.equals("3gp") || name.equals("wmv")
                || name.equals("ts") || name.equals("rmvb")
                || name.equals("mov") || name.equals("m4v")
                || name.equals("avi") || name.equals("m3u8")
                || name.equals("3gpp") || name.equals("3gpp2")
                || name.equals("mkv") || name.equals("flv")
                || name.equals("divx") || name.equals("f4v")
                || name.equals("rm") || name.equals("asf")
                || name.equals("ram") || name.equals("mpg")
                || name.equals("v8") || name.equals("swf")
                || name.equals("m2v") || name.equals("asx")
                || name.equals("ra") || name.equals("ndivx")
                || name.equals("xvid")) {
            isImageFile = true;
        } else {
            isImageFile = false;
        }
        return isImageFile;
    }

    public static String getImageName(String path) {
        String[] split = path.split("/");
        for (int i = 0; i < split.length; i++) {
            String FileEnd = split[i];
            if (FileEnd.contains("jpg") || FileEnd.contains("png") || FileEnd.contains("gif")
                    || FileEnd.contains("jpeg") || FileEnd.contains("bmp")) {
                return FileEnd;
            }
        }
        return "";
    }
    public static String getVideoName(String path) {
        String[] split = path.split("/");
        for (int i = 0; i < split.length; i++) {
            String name = split[i];
            if (name.contains(".mp4") || name.contains(".3gp") || name.contains(".wmv")
                    || name.contains(".ts") || name.contains(".rmvb")
                    || name.contains(".mov") || name.contains(".m4v")
                    || name.contains(".avi") || name.contains(".m3u8")
                    || name.contains(".3gpp") || name.contains(".3gpp2")) {
                return name;
            }
        }
        return "";
    }
